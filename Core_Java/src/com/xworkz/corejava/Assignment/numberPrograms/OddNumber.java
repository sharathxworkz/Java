package com.xworkz.corejava.Assignment.numberPrograms;

public class OddNumber {
	
	public static void main(String args[])   
	{  
		int num=30;  
		System.out.print("List of odd numbers from 1 to "+num+": ");  
		for (int i=1; i<=num; i++)   
		{  
			if (i%2!=0)   
			{  
				System.out.print(i + " ");  
			}  
		}  
	}  
} 

